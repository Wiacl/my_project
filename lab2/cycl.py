print("Найти факториал числа, введённого пользователем, с помощью цикла.", "\n")

n = int(input("\nВведите число для факториала: "))
factorial = 1
for i in range(1, n + 1):
    factorial *= i
print(f"Факториал {n} = {factorial}", "\n")

print('='*50)


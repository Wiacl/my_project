print("Запросить у пользователя число n и вывести все его делители.", "\n")

num = int(input("\nВведите число что бы посчитать его делители: "))
dividers = [i for i in range(1, num + 1) if num % i == 0]
print(f"Делители числа {num}: {dividers}"), "\n"

print('='*50)


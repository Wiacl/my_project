print("Сгенерировать последовательность Фибоначчи длиной n и вывести её.")
n = int(input("\nВведите длину последовательности Фибоначчи: "))
fib = [0, 1]
for i in range(2, n):
    fib.append(fib[i-1] + fib[i-2])
print(f"Последовательность Фибоначчи: {fib[:n]}")